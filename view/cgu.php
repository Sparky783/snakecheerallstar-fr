				<section id="cgu" class="col-sm-12">
					<h1 class="text-center">Conditions Générales d'utilisation</h1>

					<h3>Article 1 - La cotisation</h3>
					<p>
						Les membres fondateurs et les membres d'honneur ne paient pas de cotisations.<br />
						Les sportifs, les membres adhérents, suivront la grille tarifaire de leur cotisation en fonction de leur âge.<br />
						Le montant de la cotisation est voté annuellement par l'assemblée générale sur proposition du conseil d'administration.<br />
						Toute cotisation versée à l'association est définitivement acquise. Il ne saurait être exigé un remboursement de cotisation en cours d'année en cas de démission, d'exclusion, ou de décès d'un membre.
					</p>

					<h3>Article 2 - Admission de membres nouveaux</h3>
					<p>
						Les personnes désirant adhérer devront remplir un bulletin d’adhésion. Pour les mineurs de moins de seize ans, ce bulletin est rempli par le représentant légal.<br />
						Cette demande doit être acceptée par le bureau. A défaut de réponse dans les quinze jours du dépôt du bulletin d’adhésion, la demande est réputée avoir été acceptée.<br />
						Le règlement intérieur à jour est remis à chaque nouvel adhérent.
					</p>

					<h3>Article 3 - Exclusions</h3>
					<p>
						Conformément à l'article 7 des statuts, un membre peut être exclu pour les motifs suivants: 
					</p>
					<ul>
						<li>Non-présence aux réunions</li>
						<li>Matériel détérioré</li>
						<li>Comportement dangereux</li>
						<li>Propos désobligeants envers les autres membres</li>
						<li>Comportement non conforme avec l’éthique de l’association</li>
						<li>Non-respect des statuts et du règlement intérieur</li>
					</ul>
					<p>
						Celle-ci doit être prononcée par le conseil d’administration après avoir entendu les explications du membre contre lequel une procédure d'exclusion est engagée. Le membre sera convoqué par lettre commandée avec AR quinze jours avant cette réunion. Cette lettre comportera les motifs de la radiation. Lors de cette convocation il pourra se faire assister d’une personne de son choix.<br />
						La décision de la radiation sera notifiée par lettre recommandée avec AR.<br />
						Quorum : 3⁄4 des personnes du conseil d'administration.
					</p>

					<h3>Article 4 - Démission – Décès</h3>
					<p>
						Conformément à l'article 7 des statuts, le membre démissionnaire devra adresser par lettre recommandée avec AR sa décision au Conseil d’administration.<br />
						Le membre n’ayant pas réglé sa cotisation annuelle dans un délai de 4 mois à compter de la date d’exigibilité sera considéré d’office comme démissionnaire. Aucune restitution de cotisation n'est due au membre démissionnaire.<br />
						En cas de décès, la qualité de membre s'éteint avec la personne.
					</p>

					<h3>Article 5 - Remboursement des frais</h3>
					<p>
						Seuls les membres du conseil d'administration et/ou les membres élus du bureau peuvent prétendre au remboursement des frais engagés dans le cadre de leurs fonctions et sur justifications. (L'abandon des ces remboursements peut donner lieu à l'obtention d'une réduction d'impôt) 
					</p>

					<h3>Article 6 - Mesures de police</h3>
					<p>
						Il est interdit de fumer et de consommer des boissons alcoolisées dans les locaux de l’association. 
					</p>

					<h3>Article 7 - Section locale</h3>
					<p>
						Lorsqu’une activité regroupe plusieurs participants, le conseil d’administration peut décider de la création d’une section locale. Il établit alors un projet prévoyant cette création et le mode de fonctionnement de la section locale. Ce projet est ensuite ratifié par l’assemblée générale ordinaire/extraordinaire. 
					</p>

					<h3>Article 8 - Assemblée générale ordinaire</h3>
					<p>
						Conformément à l'article 15 des statuts de l'association, l'assemblée générale ordinaire se réunit dans les six premiers mois de l'année sur convocation du président.<br />
						Les membres à jour de leur cotisation sont convoqués par émail.<br />
						Le vote s'effectue par bulletin secret déposé dans l'urne tenue par le secrétaire de séance. Les mineurs ont droit de vote à partir de l’âge de 16 ans.<br />
						Toute autre décision (règlement intérieur, cotisation...) est votée à main levée.<br />
						Il est désigné un secrétaire de séance en début de réunion. Il rédige un procès-verbal de l’assemblée générale.<br />
						Les votes par procuration ou par correspondance sont autorisés.
					</p>

					<h3>Article 9 - Assemblée générale extraordinaire</h3>
					<p>
						Conformément à l'article 16 des statuts de l'association, une assemblée générale extraordinaire peut se réunir en cas de modification des statuts, hormis les articles 3 et 12 des statuts de l'association qui seront traités par le conseil, dissolution, ou fusion de l'association.<br />
						Les membres à jour de leur cotisation sont convoqués par mail.<br />
						Le vote s'effectue par bulletin secret déposé dans l'urne tenue par le secrétaire de séance. Les mineurs ont droit de vote à partir de l’âge de 16 ans.<br />
						Il est désigné un secrétaire de séance en début de réunion. Il rédige un procès-verbal de l’assemblée générale.<br />
						Les votes par procuration ou par correspondance sont interdits.<br />
						Les décisions seront prises au quorum de 3⁄4 des membres de l'association.
					</p>

					<h3>Article 10 - Le règlement intérieur</h3>
					<p>
						Le règlement intérieur est établi par le conseil d'administration conformément à l'article 16 des statuts de l'association puis ratifié par l’assemblée générale ordinaire.<br />
						Il peut être modifié sur proposition de l'assemblée générale (à la demande d'un certain nombre de membres).<br />
						Le nouveau règlement intérieur est adressé à tous les membres de l'association par mail sous un délai de 15 jours suivant la date de la modification.<br />
						Le règlement intérieur sera affiché dans les locaux de l’association.
					</p>

					<h3>Article 11 – Responsabilité</h3>
					<p>
						La responsabilité de l'association Snake cheer all star n'est engagée que lorsque les tuteurs ou le représentant légal ont confié l'enfant au coach responsable du cours sur le lieu d’entraînement ou de représentation.<br />
						Les tuteurs ou le représentant légal s'engage à amener et à venir chercher leurs enfants aux heures spécifiées des entraînements et des représentations. Les entraîneurs ne pourront être tenus responsables de tout accident survenu en dehors de ces horaires. Tout retard de début et de fin de cours non justifié pourra être sanctionné.<br />
						Aucun enfant mineur ne pourra quitter seul le lieu d’entraînement ou de représentation si les tuteurs ou le représentant légal n'ont pas signé d'autorisation.
					</p>

					<h3>Article 12 – Fonctionnement</h3>
					<p>
						Toute absence répétitive aux entraînements avant une représentation ou le championnat entraînera la non participation de l'élève.<br />
						Les coachs se réservent le droit d'effectuer des sélections en vu de championnat ou de manifestations sportives importantes.<br />
						Tout athlète étant sélectionné et souhaitant participer au championnat de France doit être présent à tous les entraînements y compris les stages supplémentaires nécessaires à la progression de l'équipe sous peine de non participation au championnat.<br />
						Les élèves sont responsables de leurs tenues et pompons, toute détérioration ou perte entraînera l'encaissement de la caution.<br />
						Les tuteurs s'engagent à ne s'immiscer, en aucun cas, dans le domaine sportif et le choix des entraîneurs (composition des équipes, tenues, déroulement des cours, mode de transport choisi...).<br />
						Tous déplacements sportifs organisés par le club nécessitant la prise d'un transport en commun est obligatoire sous peine de non participation de l'élève à la compétition.<br />
					</p>
					
					<h3>Article 13 - Sécurité</h3>
					<p>
						Pour des raisons de sécurité les élèves ne doivent pas porter de bijoux, dont piercing. Les ongles doivent être coupés et les cheveux attachés. Les chewing-gum sont interdits.<br />
						En cas d'accident, il sera fait appel aux services d'urgence.<br />
						Les élèves devront porter une tenue sportive près du corps ( pas de tee-shirts amples, ni de capuches) et des baskets de salle.<br />
						L'association décline toute responsabilité en cas de détérioration, vol ou perte des effets personnels.<br />
						Les membres et leurs représentants s'engagent à ne prendre aucune initiative ou décision pouvant engager la responsabilité de l'association sous peine de sanctions.<br />
						Pour toute parution personnelle dans la presse ou sur internet, un accord préalable du club sera nécessaire.<br />
						Chaque membre, chaque tuteur s'engage lors de son adhésion à l'association à respecter le présent règlement. Tout manquement fera l'objet de sanctions définies et appliquées par l'équipe dirigeante qui se réserve le droit de prononcer des sanctions pouvant aller jusqu'à l'exclusion temporaire ou définitive du club.
					</p>
